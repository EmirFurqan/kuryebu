// Helper functions to get display names
export const getDistrictName = (slug) => {
    const location = locations.find(l => l.district === slug);
    return location ? location.displayName : slug.charAt(0).toUpperCase() + slug.slice(1);
};

export const getNeighborhoodName = (string) => {
    return string
        .split('-')
        .map(word => word.charAt(0).toUpperCase() + word.slice(1))
        .join(' ');
};
